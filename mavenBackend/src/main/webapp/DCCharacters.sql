CREATE TABLE DC_CHARACTER(
    CHARACTER_ID INT PRIMARY KEY,
    CHARACTER_NAME VARCHAR2(100) UNIQUE,
    CHARACTER_AGE INT,
    CHARACTER_POWERS VARCHAR2(4000)
);
