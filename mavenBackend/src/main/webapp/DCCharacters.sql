--TRIGGERS
CREATE TRIGGER TR_DC_CHAR
BEFORE INSERT ON DC_CHARACTER
FOR EACH ROW
BEGIN
    SELECT SQ_DC_CHAR_PK.NEXTVAL INTO :NEW.CHARACTER_ID FROM DUAL;
END;
