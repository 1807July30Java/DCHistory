package com.revature.service;

public class Character {
	private String name;
	private String age;
	private String powers;
	private String gender;
	
	public Character(String name, String age, String powers, String gender) {
		super();
		this.name = name;
		this.age = age;
		this.powers = powers;
		this.gender = gender;
	}
}
